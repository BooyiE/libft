#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t a;
	size_t b;

	b = 0;
	a = 0;
	while (s1[a] != '\0')
		a++;
	while (s2[b])
	{
		s1[a] = s2[b];
		b++;
		a++;
	}
	s1[a] = '\0';
	return (s1);
}
