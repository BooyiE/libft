#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*a;
	unsigned char	*b;

	a = (unsigned char *)s1;
	b = (unsigned char *)s2;
	i = 0;
	while (n--)
     {
         if ((*a > *b) || (*a < *b))
             return (*a - *b);
         a++;
         b++;
	}
     return 0;
}
